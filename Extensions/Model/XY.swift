import UIKit

struct XY: Equatable {
    @Clampped(range: 0...0.9) var x: Double = 0
    @Clampped(range: 0...0.9) var y: Double = 0
    
    init(x: Double, y: Double) {
        self.x = x.isNaN ? 0 : x
        self.y = y.isNaN ? 0 : y
    }
}

extension XY {
    
    var uiColor: UIColor {
        UIColor(xy: self)
    }
    
    /// 约束XY
    func constrained(in xyRanges: XYRanges) -> XY {
        let newXY = XY(x: xyRanges.xRange << x, y: xyRanges.yRange << y)
        if newXY != self {
            return newXY
        } else {
            return self
        }
    }
    
    /// 如果当前xy在CIE1931马蹄图内则返回自身, 否则返回马蹄图边界上最近的点
    fileprivate var constrainedToHorseShoe: XY {
        if isInsideHorseShoe { return self }
        let min = XY.horseShoeEdgeXYs.min {
            squaredDistance(to: $0) < squaredDistance(to: $1)
        }
        return min ?? .zero
    }
    
    /// 射线法判断当前xy是否在马蹄图封闭多边形内(首尾相连即紫线)
    fileprivate var isInsideHorseShoe: Bool {
        let polygon = Self.horseShoeEdgeXYs
        let n = polygon.count
        guard n >= 3 else { return false }
        var inside = false
        var j = n - 1
        for i in 0..<n {
            let xi = polygon[i].x, yi = polygon[i].y
            let xj = polygon[j].x, yj = polygon[j].y
            if ((yi > y) != (yj > y)) && (x < (xj - xi) * (y - yi) / (yj - yi) + xi) {
                inside = !inside
            }
            j = i
        }
        return inside
    }
    
    fileprivate func squaredDistance(to other: XY) -> Double {
        let dx = x - other.x
        let dy = y - other.y
        return dx * dx + dy * dy
    }
    
    /// 指定CGSize, 返回对应的坐标点, size比例必须为1:1
    func point(for size: CGSize) -> CGPoint {
        let xPercent = x / 0.9
        let yPercent = 1.0 - y / 0.9
        return CGPoint(x: size.width * xPercent, y: size.height * yPercent)
    }
    
